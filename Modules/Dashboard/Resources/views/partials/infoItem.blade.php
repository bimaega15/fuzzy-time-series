                    <h5 class="text-center">
                        Fuzzy Time Series Chan
                    </h5>
                    <p>
                        Model Fuzzy Time Series Chan adalah salah satu pendekatan dalam analisis deret waktu yang menggunakan teori himpunan fuzzy untuk menangani data deret waktu yang bersifat tidak pasti atau samar. Model ini dikembangkan oleh Ching-Chang Chan pada tahun 1998 dan bertujuan untuk meningkatkan keakuratan peramalan dengan mengintegrasikan konsep fuzzy logic.
                    </p>

                    <p>
                        Berikut adalah langkah-langkah umum dalam model Fuzzy Time Series Chan:
                    </p>
                    
                   <ul>
                    <li> Fuzzifikasi Data: Data deret waktu historis diubah menjadi nilai fuzzy menggunakan himpunan fuzzy. Misalnya, data suhu harian dapat dikategorikan ke dalam beberapa himpunan fuzzy seperti "rendah", "sedang", dan "tinggi".</li>

                    <li>
                        Pembentukan Relasi Fuzzy: Mengidentifikasi pola atau hubungan antar data dalam bentuk aturan fuzzy. Relasi ini menggambarkan bagaimana satu himpunan fuzzy berubah menjadi himpunan fuzzy lainnya dari waktu ke waktu.
                    </li>

                    <li>
                        Membangun Matriks Relasi Fuzzy: Membuat matriks yang mencerminkan kekuatan hubungan antara himpunan fuzzy berdasarkan data historis.
                    </li>
                    <li>
                        Defuzzifikasi: Mengubah hasil prediksi yang berbentuk nilai fuzzy kembali ke nilai crisp (nilai sebenarnya) untuk interpretasi yang lebih mudah.
                    </li>
                    <li>
                        Peramalan: Menggunakan model yang telah dibentuk untuk meramalkan nilai masa depan dari data deret waktu.
                    </li>
                   </ul>               
                   <p>
                    Kelebihan dari model ini adalah kemampuannya untuk menangani data dengan pola yang tidak pasti dan memberikan hasil peramalan yang lebih fleksibel dan adaptif dibandingkan metode deret waktu konvensional.
                   </p>
